// while: Es un bucle que se ejecuta mientras la condición sea verdadera.
// Se evalúa la condición antes de ejecutar el código.
// Si la condición es falsa, el código dentro del bucle no se ejecuta.
// Si la condición es verdadera, el código dentro del bucle se ejecuta.
// Al final de cada ejecución, la condición se evalúa nuevamente.
// Si la condición es falsa, el bucle se detiene.

while(condicion){
    // código a ejecutar
}

// Ejemplo
let contador = 0;
while(contador < 5){
    console.log(contador);
    contador++;
}